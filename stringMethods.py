name = "Asfar Waheed"
print(name.upper())  # returns a new string so the original string is not altered
print(name.lower())
print(name.title())  # Capitalizes the first letter of each word
print(name.strip())  # Removes leading and trailing whitespace
print(name.lstrip())  # Removes leading whitespace
print(name.rstrip())  # Removes trailing whitespace
print(name.find("Asf"))
# Replaces a substring with another substring
print(name.replace("Asfar", "Asf"))
print(name)
print("As" in name)  # Checks if a substring exists in the string
print("new" not in name)  # Checks if a substring does not exist in the string
